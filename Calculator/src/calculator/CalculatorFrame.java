    private void equalsBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalsBtnActionPerformed
        // TODO add your handling code here:
            if (!calTextField.getText().equals("")){
                firstVal = Integer.parseInt(calTextField.getText()); 
                seccondVal = Integer.parseInt(calMemTextField.getText());
                
                    switch (operand) {
                    case "+":
                        resultVal = seccondVal + firstVal;
                        calMemTextField.setText(""); 
                        calTextField.setText(String.valueOf(resultVal));
                        break;
                    case "-":
                        resultVal = seccondVal - firstVal;
                        calMemTextField.setText(""); 
                        calTextField.setText(String.valueOf(resultVal));
                        break;
                    case "/":
                        if (firstVal != 0 && seccondVal != 0){
                        resultVal = seccondVal / firstVal;
                        calMemTextField.setText(""); 
                        calTextField.setText(String.valueOf(resultVal));
                        } else {
                          JOptionPane.showMessageDialog(null,"Invalid operation, you cannot divide by zero.","Warning",JOptionPane.WARNING_MESSAGE);
                        }
                        break;
                    case "*":
                        resultVal = seccondVal * firstVal;
                        calMemTextField.setText(""); 
                        calTextField.setText(String.valueOf(resultVal));
                        break;    
                    default:
                        break;            
                        }
            } 
    }//GEN-LAST:event_equalsBtnActionPerformed

